{
	  'nvimdev/dashboard-nvim',
	  event = 'VimEnter',
 	 config = function()
 	   require('dashboard').setup {
      			theme='doom',

      			disable_move=true,
 		     	config = {
				header=PickRandomHeader(myTableOfHeaders),
				footer={'Shabingus'}
			}
		}	

	end,
  dependencies = { {'nvim-tree/nvim-web-devicons'}}
}
